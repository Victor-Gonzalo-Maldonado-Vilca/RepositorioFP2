public class Ejercito {
  private int edad;
  private char genero;
  private String DNI;
  public void setEdad(int edad){
    this.edad = edad;
  }
  public void setGenero(char genero){
    this.genero = genero;
  }
  public void setDNI(String DNI){
    this.DNI = DNI;
  }
  public int getEdad(){
    return edad;
  }
  public char getGenero(){
    return genero;
  }
  public String getDNI(){
    return DNI;
  }
}
